int Posn::whichChamber() {
  if (row >= 3 && row <= 6 && col >= 3 && col <= 28) return 1;
  if (row >= 15 && row <= 21 && col >= 4 && col <= 24) return 2;
  if (row >= 10 && row <= 12 && col >= 38 && col <= 49) return 3;
  if (row >= 3 && row <= 4 && col >= 39 && col <= 60) return 4;
  if (row == 5 && col >= 39 && col <= 69) return 4;
  if (row == 6 && col >= 39 && col <= 72) return 4;
  if (row >= 7 && row <= 12 && col >= 61 && col <= 75) return 4;
  if (row >= 16 && row <= 18 && col >= 65 && col <= 75) return 5;
  if (row >= 19 && row <= 21 && col >= 37 && col <= 75) return 5;
  return -1;
}